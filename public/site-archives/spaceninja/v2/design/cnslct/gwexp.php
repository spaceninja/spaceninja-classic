<blockquote>GroupWise Expiration Policy applies to your emails,
attachments, phones, appointments, tasks and notes but NOT to your acutal
account.&nbsp; What it means is that your account does not expire under
this policy just your old emails, attachments, phones, appointments, tasks
and notes.</blockquote>

<dir>The following Expiration Policy will be applied to all
GroupWise Users on IRN:&nbsp;</dir>
<b><font size=+1>Students, GA, RA, TA, and Mentors:</font></b>
<blockquote>Emails, attachments, phones, appointments, tasks
and notes older than <b><font color="#0000FF">30</font></b> days will be
purged automatically from the server.</blockquote>
<b><font size=+1>Faculty, and staff:</font></b>
<dir>Emails, attachments, phones, appointments, tasks and
notes older than <b><font color="#0000FF">90</font></b> days will be purged
automatically from the server.</dir>
If you would like to keep your important items you can either
archive them or save them as individual files. In order to prepare your
GroupWise account for Archive, you need to follow these instructions.
<p><b><font size=+1>PC Users:</font></b>
<ul>
<li>
create a folder called archive in your home directory. i.e.
f:\mystuff\my_context\my_loginid\archive&nbsp;</li>

<li>
open GroupWise client&nbsp;</li>

<li>
click on tools and select options&nbsp;</li>

<li>
double-click on the environment icon and choose the file
location tab&nbsp;</li>

<li>
enter the path of directory you created in step1 above in
the archive directory field, or browse to that directory on the network
by clicking on the folder icon (<b>if you are using one of the computers
in a lab, then you need to check and reset this preference, if needed,
each time you login since the lab computers are used by more than 1 user
and rebuild periodically</b>)&nbsp;</li>

<li>
select "cleanup" tab&nbsp;</li>

<br>if you login to GroupWise only from your computer and
NOT any other computer then:&nbsp;
<ul>select "auto-archive after:" and set the days to less
than 30 (for students, ga, ra, ta, and mentors) and less than 90 (for faculty,
and staff) for both mail and appointment. <b>if you set the days to more
than specified days, your mail, attachment, phone, appointment, task and
note will be expired before it gets archived and it will be purged from
the system automatically.&nbsp; Purged items are <u>NOT</u> recoverable.</b></ul>
</ul>

<ul>
<li>
if you ever login to GroupWise from a computer different
than yours, then select "Manual delete and archive" since the automatic
archive happens before you get the chance to reset the path to your archive
as described above.&nbsp; With manual archive you can set the path and
then perform the archive when you are ready.&nbsp; <u>But you need to remember
to perform the archive yourself every week.</u>&nbsp; To do the archive
manually select the items that you want to archive, then select Archive
from the Actions menu.&nbsp;</li>

<li>
"empty trash" is set to automatic after 7 days&nbsp;</li>

<li>
click on ok and then close.&nbsp;</li>
</ul>
<b><font size=+1>Mac users:</font></b>
<ul>
<li>
create a folder called archive in your home directory. i.e.
network\mystuff\my_context\my_loginid\archive&nbsp;</li>

<li>
open GroupWise client&nbsp;</li>

<li>
click edit preferences&nbsp;</li>

<li>
click environment click the general pop-up menu click file
location&nbsp;</li>

<li>
specify the location of your archive directory in the archive
directory text box using the FIND button. (<b>if you are using one of the
computers in a lab then you need to check and reset this preference, if
needed, each time you login since the lab computers are used by more than
1 user and rebuild periodically.</b>)&nbsp;</li>

<li>
Click the File Location pop-up menu click Cleanup&nbsp;</li>

<br>if you login to GroupWise only from your computer and
NOT any other computer then:&nbsp;
<ul>select "auto-archive after:" and set the days to less
than 30 (for students, ga, ra, ta, and mentors) and less than 90 (for faculty,
and staff) for both mail and appointment. <b>if you set the days to more
than specified days, your mail, attachment, phone, appointment, task and
note will be expired before it gets archived and it will be purged from
the system automatically.&nbsp; Purged items are <u>NOT</u> recoverable.</b></ul>
</ul>

<ul>
<li>
if you ever login to GroupWise from a computer different
than yours, then select "Manual delete and archive" since the automatic
archive happens before you get the chance to reset the path to your archive
as described above.&nbsp; With manual archive you can set the path and
then perform the archive when you are ready.&nbsp; <u>But you need to remember
to perform the archive yourself every week.</u>&nbsp; To do the archive
manually select the items that you want to archive, then select Archive
from the Actions menu&nbsp;</li>

<li>
"Empty trash" is set to Automatic after 7 days&nbsp;</li>

<li>
Click OK click CLOSE.</li>
</ul>

