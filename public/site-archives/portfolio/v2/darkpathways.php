<p>The record label Crunch Pod approached me to design a three CD series called Dark Pathways. They wanted the design for each CD to be able to stand on its own, while being easily associated with the others. The third CD was dropped due to time constraints, but the first two were finalized and taken to press.</p>

<p>Each CD had a dominant color for the design, and they all shared similar imagery taken from mechanical drawings, bus maps and circuit diagrams. The CDs are available for purchase through <a href="http://www.crunchpod.com/">Crunch Pod</a>.

