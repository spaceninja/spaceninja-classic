<P><FONT SIZE=4><B>Who are we?</B></FONT>
<br>Computing and Network Services (CNS) is the technical systems division of the Office of Information Technology (<a href="http://www.oit.pdx.edu/">OIT</a>). CNS manages the campus network and Internet services, central computers like Loki and Odin, central Web and E-mail servers, and departmental or special purpose servers.  CNS is also responsible for integrating and maintaining software on PSU's central systems and Lab and Classroom computers.

<UL>
CNS has five technical teams:
<LI>the <a href="cnsnet.php">Network</a> Team
<LI>the <a href="cnsunix.php">UNIX</a> Team
<LI>the <a href="cnsops.php">Data Center Operations</a> Team
<LI>the <a href="cnsnov.php">Server</a> Team
<LI>the <a href="cnslct.php">Labs and Classrooms</a> Team.
</UL>

<P><FONT SIZE=4><B>Mission</B></FONT>
<BR>To serve our clients through a unified network of facilities, including labs, development areas, high technology classrooms, and staff offices. 

<P>We at CNS are committed to providing the University with the finest quality in service, support and technology for use in instruction and research.

<P><FONT SIZE=4><B>Second Tier Services and Support</B></FONT>
<BR>CNS is what is referred to as "Tier 2" Support. Tier 1 Support interacts directly with the end user (like the <a href="http://www.helpdesk.pdx.edu/">help desk</a>). Tier 2 Support interacts directly with Tier 1.

<P>Most staff and faculty will call the <a href="http://www.helpdesk.pdx.edu/">Help Desk</a> (5-HELP) before calling CNS, but if you need direct support from one of the teams or have a specific question you can reach our main office at 5-4441.

