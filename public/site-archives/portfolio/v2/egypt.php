<p>For my Graphic Design II class at Portland State University, we were asked to produce a poster advertising an event from around town. I chose an exhibit from the Oregon Museum of Science and Industry called "Mysteries of Egypt". Our only requirements were the poster dimensions, the logos to be included on the poster, and to use a four-color print job. I decided to focus on the mysterious aspect of the title, and designed very ghostly imagery and logotype.</p>

